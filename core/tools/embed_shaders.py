import sys, pathlib

def require_within_root(path: pathlib.Path, root: pathlib.Path, label: str) -> pathlib.Path:
    resolved = path.resolve()
    root_resolved = root.resolve()
    try:
        resolved.relative_to(root_resolved)
    except ValueError:
        raise ValueError(f"{label} must be within {root_resolved}: {resolved}")
    return resolved

ROOT_DIR = pathlib.Path.cwd().resolve()
SHADER_DIR = require_within_root(pathlib.Path(sys.argv[1]), ROOT_DIR, "SHADER_DIR")
OUTPUT_FILE = require_within_root(pathlib.Path(sys.argv[2]), ROOT_DIR, "OUTPUT_FILE")

if not SHADER_DIR.is_dir():
    raise ValueError(f"SHADER_DIR is not a directory: {SHADER_DIR}")

# Must always appear in the same order for predictability
files = sorted(SHADER_DIR.glob("*.wgsl"))

shader_strings = ""
shader_array_contents = ""
for file in files:
    name = file.stem
    text = file.read_text()

    shader_strings += f'    constexpr char {name}[] = R"WGSL(\n{text}\n)WGSL";\n\n'
    shader_array_contents += f'        {{"{name}", {name}}},\n'

# We need to use {{ and }} to escape the f-string expansion
file_contents = f"""// Do not edit this file
// core/tools/embed_shaders.py manages the contents of this file

#ifndef EMBEDDED_SHADERS_H
#define EMBEDDED_SHADERS_H

#include <array>
#include <string_view>
#include <string>

namespace embedded_shaders {{
    struct ShaderEntry {{
      const std::string_view id;
      const std::string_view source;
    }};

{shader_strings}

    constexpr std::array<ShaderEntry, {len(files)}> shaders = {{{{
{shader_array_contents}
    }}}};
}}  // namespace embedded_shaders

#endif // EMBEDDED_SHADERS_H
"""

# Only write if the file doesn't exist or contents differ
if not OUTPUT_FILE.exists() or OUTPUT_FILE.read_text() != file_contents:
    OUTPUT_FILE.write_text(file_contents)
    print(f"Writing to {OUTPUT_FILE}")
else:
    print(f"No changes in {OUTPUT_FILE}, skipping write")
